#include <stdlib.h>
#include "game_2048.h"

void		rnd_nbr(u_int *map)
{
	int		x;

	while (42)
	{
		x = rand() % (MAPSIZE);
		if (map[x] != 0)
			continue ;
		map[x] = rand() % 2 + 1;
		break ;
	}
}
